	<h1 style="text-align: center;">What's in the photo?</h1>

	<p style="text-align: center;"><img id="user_model_img_3" src="images\Splash_Flash.png" style="height:400px;" alt="E Knight-Rowley S1802083" title="E Knight-Rowley S1802083"> </p>

	<p style="text-align: justify;">“What’s in The photo?” uses a hands-on, practical approach that goes the extra step beyond theory work.
		Instead of poring over textbooks or skimming articles for hours, you can learn how Machine Learning (ML) works by training and testing your very own image-identifying ML models!
		<br><br>
		<strong><U>So what does this mean?</U></strong>
		<br>
		What’s in the photo? Is designed to allow you to train your own image classification models and run them right here on the website!
		<br>
		<ul>
			<li>
			See how a completed image-identification model works with our range of pre-trained models, available right within your model library
			</li>
			<li>
			Create your own models and train them to recognise objects - you can use your own images to do this, get some help from our image downloading bot, or use a combination of both!
			</li>
			<li>
			Test and run the models you create and watch them analyse new images to identify the object you’ve trained them to spot
			</li>
		</ul>
		
		<strong><u>TensorFlow</u></strong>
		<br>
		If you've ever looked into ML in the past, the chances are that you've come across the term TensorFlow. 
		<br>
		So what is it?
		<br>
		TensorFlow is an open source framework that makes getting into ML accessible to everyone and easier than ever!
		<br>
		Since TensorFlow is open source, it has a wide array of libraries available to help you achieve your goals. What's in the photo? Makes use of this by developing it's core system on the Keras library.
		<br><br>
		To read more on TensorFlow you can find information on their <a href="https://www.tensorflow.org/">official website</a>.
		<br><br>
		<strong><U>Keras</U></strong>
		<br>
		Machine Learning is <i> complicated </i> and makes use of very complex maths. 
		
		TensorFlow removes a lot of that complication by providing a framework to develop from - lowering the starting knowledge required to build a good solution.
		...It's still pretty alien to people that aren't heavily in the field though.
		
		Keras is a framework built on top of TensorFlow which adds another layer of abstraction - once again lowering this barrier to entry and providing a (relatively!) simple foundation to start learning and developing Machine Learning solutions.
		
		TensorFlow is written at core in the hyper efficient "C" programming language and Keras provides a Python wrapper which is used behind the scenes in What's In The Box - as the brains behind the whole show!
